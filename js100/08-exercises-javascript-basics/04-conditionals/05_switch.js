let animal = 'horse';

switch (animal) {
  case 'duck':
    console.log('quack');
  case 'squirrel':
    console.log('nook nook');
  case 'horse':
    console.log('neigh');
  case 'bird':
    console.log('tweet tweet');
  default:
    console.log('*cricket*');
}


// will log neigh, tweet tweet and *cricket*
// UPDATE: will search for case: horse then execute all other cases since there is no break statement.